package com.sciome.opera.model.training;

public class RegParam
{
	Double	r2;
	Double	r;
	Double	rmsec;
	
	public Double getR2() {
		return r2;
	}
	public void setR2(Double r2) {
		this.r2 = r2;
	}
	public Double getR() {
		return r;
	}
	public void setR(Double r) {
		this.r = r;
	}
	public Double getRmsec() {
		return rmsec;
	}
	public void setRmsec(Double rmsec) {
		this.rmsec = rmsec;
	}
}
